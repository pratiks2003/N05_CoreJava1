package met.tns.operator;

public class BitWiseOperator {

	public static void main(String[] args) {
    //bit wise operator
	int x ,y ;
	
	x = 8;
	y = 1;
	
	System.out.println(x&y);
	System.out.println(x|y);
	System.out.println(x>>y);
	System.out.println(x<<y);
	System.out.println(x^y);

		
		
		
	}

}
